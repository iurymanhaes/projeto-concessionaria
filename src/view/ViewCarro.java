    private void btn_atuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_atuaActionPerformed
        // TODO add your handling code here:

        if (jTCarros.getSelectedRow() != -1) {

            Carro c = new Carro();
            CarroDAO cdao = new CarroDAO();

            c.setModelo(txtModelo.getText());
            c.setAno(Integer.parseInt(txtAno.getText()));
            c.setPlaca(txtPlaca.getText());
            c.setValor(Float.parseFloat(txtValor.getText()));

            c.setId((int) jTCarros.getValueAt(jTCarros.getSelectedRow(), 0));
            cdao.update(c);

            txtModelo.setText("");
            txtAno.setText("");
            txtPlaca.setText("");
            txtValor.setText("");
            lerTabelaCarro();

        }
    }//GEN-LAST:event_btn_atuaActionPerformed

    private void btn_dltActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dltActionPerformed
        // TODO add your handling code here:

        if (jTCarros.getSelectedRow() != -1) {

            Carro c = new Carro();
            CarroDAO cdao = new CarroDAO();

            c.setId((int) jTCarros.getValueAt(jTCarros.getSelectedRow(), 0));

            cdao.delete(c);

            txtModelo.setText("");
            txtAno.setText("");
            txtPlaca.setText("");
            txtValor.setText("");
            lerTabelaCarro();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um carro para excluir.");
        }
    }//GEN-LAST:event_btn_dltActionPerformed

