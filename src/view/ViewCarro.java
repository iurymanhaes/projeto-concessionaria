    public void lerTabelaCarro() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTCarros.getModel();
        modelo.setNumRows(0);
        CarroDAO cdao = new CarroDAO();

        cdao.ler().forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getId(),c.getModelo(),c.getAno(),c.getPlaca(),c.getValor()
            });
        });

    }
    public void pesquisarNoBanco(String l){
        
        DefaultTableModel modelo = (DefaultTableModel) jTCarros.getModel();
        modelo.setNumRows(0);
        CarroDAO cdao = new CarroDAO();

        cdao.pesquisaModelo(l).forEach((c) -> {
            modelo.addRow(new Object[]{
                c.getId(),
                c.getModelo(),
                c.getAno(),
                c.getPlaca(),
                c.getValor()
            });
        });

    }

